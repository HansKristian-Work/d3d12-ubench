import os
import sys

def list_entry_points(path):
    entries = []
    parsed_attr = False
    with open(path, 'r') as f:
        lines = f.readlines()
        for line in lines:
            if len(line) == 0 or line == '\n':
                continue
            if ('void ' in line) and parsed_attr:
                split = line.split('(', 1)
                split = split[0].split(' ')[1]
                entries.append(split)
            parsed_attr = line[0] == '[' and line[-2] == ']'
    return entries

def main():
    entries = []
    files = os.listdir('.')
    for file in files:
        split = os.path.splitext(file)
        if split[1] == '.hlsl':
            entries += [(x, split[0]) for x in list_entry_points(file)]

    with open(os.path.join(sys.argv[1], 'tests.cmake'), 'w') as f:
        f.write('# This file is autogenerated by generate.py!\n')
        for entry in entries:
            print(f'Adding test {entry[0]} from {entry[1]}.hlsl')
            f.write(f'compile_hlsl({entry[0]} {entry[1]})\n')

    with open(os.path.join(sys.argv[1], 'shaders.hpp'), 'w') as f:
        f.write('#pragma once\n// This file is autogenerated by generate.py!\n')
        f.write('#include "device.hpp"\n')
        f.write('namespace DXIL {\n')
        f.write('struct Shaders {\n')
        f.write('\tShaders();\n')
        for entry in entries:
            f.write(f'\tPipelineState {entry[0]};\n')
        f.write('};\n')
        f.write('}\n')

    with open(os.path.join(sys.argv[1], 'shaders.cpp'), 'w') as f:
        f.write('// This file is autogenerated by generate.py!\n')
        f.write('#include "shaders.hpp"\n')
        f.write('namespace DXIL {\n')

        for entry in entries:
            f.write(f'#include "{entry[0]}.inc"\n')
        f.write('\n')

        f.write('Shaders::Shaders() {\n')
        for entry in entries:
            f.write(f'\t{entry[0]}.bytecode = g_{entry[0]};\n')
            f.write(f'\t{entry[0]}.bytecode_size = sizeof(g_{entry[0]});\n')
            f.write(f'\t{entry[0]}.name = "{entry[0]}";\n')
        f.write('}\n')
        f.write('}\n')

if __name__ == '__main__':
    main()
